function draw5x7(ctx, text, x, y, pitch=10, dot=3, color="#ff7a00"){
  
  ctx.save();
  ctx.shadowColor = "rgba(255,140,30,0.6)";
  ctx.shadowBlur = 8;
  for(let ch of text){
    const cols = FONT5x7[ch] || FONT5x7[" "];
    for(let cx=0; cx<5; cx++){
      const col = cols[cx];
      for(let cy=0; cy<7; cy++){
        if(col & (1<<cy)){
          const px = x + cx*pitch;
          const py = y + cy*pitch;
          ctx.beginPath();
          ctx.fillStyle = color;
          ctx.arc(px, py, dot, 0, Math.PI*2);
          ctx.fill();
        }
      }
    }
    x += 6*pitch; // 5 columns + 1 spacing
  }
  ctx.restore();
}
function measure5x7(text, pitch=10){ return text.length * 6 * pitch; }

