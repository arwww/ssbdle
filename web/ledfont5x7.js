// Zeichnet ein Sprite beliebiger Größe
function drawSpriteRows(ctx, rows, x, y, pitch=10, dot=3, color="#ff7a00"){
  ctx.save();
  ctx.shadowColor = "rgba(255,140,30,0.6)";
  ctx.shadowBlur = 8;
  ctx.fillStyle = color;
  const h = rows.length;
  const w = rows[0].length;
  for(let ry=0; ry<h; ry++){
    const line = rows[ry];
    for(let rx=0; rx<w; rx++){
      if(line[rx] === "1"){
        const px = x + rx*pitch;
        const py = y + ry*pitch;
        ctx.beginPath();
        ctx.arc(px, py, dot, 0, Math.PI*2);
        ctx.fill();
      }
    }
  }
  ctx.restore();
}
function measureSpriteRows(rows, pitch=10){
  return rows[0].length * pitch;
}




